
#metodos para operaciones matematicas  módulo built-in, (o módulo estándar). Esto significa que no necesitas importar 
# ninguna biblioteca adicional para utilizarla.

x = 3.14
y = -2
z = 8

#redondear numero
result = round(x)
#obtene valor absoluto
result = abs(y)
#obtener numeero elevado a otro numero
result = pow(5,2,3)
#obtener el numero mayor de una lista de numeros
result = max(x,y,z)
#obtener el numero menor de una lista de numeros 
result = min(x,y,z)

print(result)